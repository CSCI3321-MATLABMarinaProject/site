<h1>while Loops</h1>

<p>
<strong>Q. What is a while loop?</strong><br>
A. A while loop is an iteration structure that repeats a set of instructions over and over again as long as a given condition is true. The repetition stops when the given condition is false.<br>
Example application: Coach to team &ndash; &ldquo;Run around this field if you can (are not tired or injured) and keep running till you cannot do so anymore (are tired or injured).&rdquo; <br>
